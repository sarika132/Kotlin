package com.example.myapplication

fun main() {
    // Mutable variable
    // We can reassign values to mutable variables
    var age = 20
    println("Initial age: $age")

    age = 25
    println("Updated age: $age")

    // Immutable variable
    // We can't reassign values to immutable variables
    val roll = 15
    println("Roll number: $roll")

}
